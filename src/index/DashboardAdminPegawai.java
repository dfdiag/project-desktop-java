    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        String nama = txt_search.getText();
        if (txt_search.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Silahkan isi dahulu kolom \npencarian dengan username");
        } else {
            try {
                Statement statement = (Statement) Connect.ConfigDB().createStatement();
                statement.executeUpdate("delete from data_karyawan where username= ('" + nama + "');");
                showTable();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
            } catch (HeadlessException | SQLException t) {
                JOptionPane.showMessageDialog(null, "Data gaagl dihapus");
            }
        }
    }//GEN-LAST:event_btnhapusActionPerformed

