    private void showTable() {
        tabelcd = new DefaultTableModel();
        table.setModel(tabelcd);
        tabelcd.addColumn("Username");
        tabelcd.addColumn("Password");
        tabelcd.addColumn("Nama");
        tabelcd.addColumn("Alamat");
        tabelcd.addColumn("KTP");
        tabelcd.addColumn("Telepon");
        tabelcd.addColumn("Jenis Kelamin");
        tabelcd.addColumn("Level");

        // menampilkan database ke dalam tabel
        try {
            String sql = "SELECT * FROM data_karyawan";
            java.sql.Connection conn = (Connection) Connect.ConfigDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                tabelcd.addRow(new Object[]{
                    res.getString("username"),
                    res.getString("password"),
                    res.getString("nama"),
                    res.getString("alamat"),
                    res.getString("no_ktp"),
                    res.getString("no_telpn"),
                    res.getString("jenis_kelamin"),
                    res.getString("level"),});
            }
            table.setModel(tabelcd);
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

